void fancycls(void) {
  // vertical line
  uint8_t i,j,w;
  j=0;
  //for(ii=20;ii>0;ii--) {
  for(j=0;j<0x80-8;j++) {
    for(i=0;i<64;i++) {
      for(w=0;w<8;w++) {
        //oled_delpixel((j-1+w)&0x7f,16+i);
        //oled_setpixel((j+w)&0x7f,16+i);
        oled_delpixel((j-1+w)&0x7f,i);
        oled_setpixel((j+w)&0x7f,i);
      }
    }
    //j=(j+1) & 0x7f;
    oled_refresh();
    //uDelay(1);
  }
  //oled_clear();
  for(j=0x80;j>8;j--) {
    for(i=0;i<64;i++) {
      for(w=0;w<8;w++) {
        //oled_delpixel((j-1+w)&0x7f,16+i);
        //oled_setpixel((j+w)&0x7f,16+i);
        oled_setpixel((j-1-w)&0x7f,i);
        oled_delpixel((j-w)&0x7f,i);
      }
    }
    //j=(j+1) & 0x7f;
    oled_refresh();
    //uDelay(1);
  }
  oled_clear();
}

