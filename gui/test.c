void test(void) {
  uint8_t refresh=1, keys;
  char *menuitems[]={"Switch Mode", "Test Pitchfork", "Key Exchange", "PGP Words"};
  uint8_t rf=0, mode=CRYPTO, tmp, chrg=0;
  char menuidx=0;
  int i;
  uint8_t pgpwords[PGPWORDS_SIZE];

  // logo + clearscreen "anim"
  LZG_Decode(bitmap_lzg, BITMAP_LZG_LEN, frame_buffer, 1024);
  //dmacpy32(frame_buffer, (uint8_t*) 0x0800ee54, 1024);
  //dmawait();
  oled_refresh();
  LZG_Decode(pgpwords_lzg, PGPWORDS_LZG_LEN, pgpwords, PGPWORDS_SIZE);

  nrf24_init();
  init_chrgstat();

  mDelay(300);
  oled_cmd(0xa7);//--set inverted display
  oled_refresh();
  mDelay(300);
  oled_cmd(0xa6);//--set normal display
  fancycls();

  UserRecord* userec = get_userrec();
  rsp_dump((uint8_t*) &userec, 4);
  rsp_detach();


  while(1){
    // info
    // battery status
    // rf powered?
    // current mode
    tmp = nrf_read_reg(STATUS);
    if(tmp != 0 && rf==0) {
      refresh=1;
      rf=1;
    } else if(tmp==0 && rf==1) {
      refresh=1;
      rf=0;
    }

    if(dual_usb_mode == CRYPTO && mode==DISK) {
      refresh=1;
      mode=CRYPTO;
    } else if(dual_usb_mode == DISK && mode==CRYPTO) {
      refresh=1;
      mode=DISK;
    }

    tmp=gpio_get(GPIOA_BASE, 2);
    if(tmp && chrg==0) {
      refresh=1;
      chrg=1;
    } else if(!tmp && chrg==1) {
      refresh=1;
      chrg=0;
    }

    // menu
    keys = key_handler();
    if((keys & UP) && (menuidx>0)) {
      menuidx--;
      refresh=1;
    }
    if((keys & DOWN) && (menuidx<MENU_LEN-1)) {
      menuidx++;
      refresh=1;
    }

    if(keys & RIGHT) {
      switch(menuidx) {
      case MENU_SWITCH_MODE: {
        switch(dual_usb_mode) {
        case CRYPTO: { storage_mode(); mode=DISK; break; }
        case DISK: { crypto_mode(); mode=CRYPTO; break; }
        }
        refresh=1;
      }
      case MENU_TEST: { test_hw(); refresh=1; break; }
      }
    }
    if(keys & LEFT) {
      oled_clear();
      refresh=1;
    }

    if(refresh) {
      if(rf) {
        oled_print(0,56, (char*) "R", Font_8x8);
      } else {
        oled_print(0,56, (char*) "r", Font_8x8);
      }
      if(chrg) {
        oled_print(8,56, (char*) "c", Font_8x8);
      } else {
        oled_print(8,56, (char*) "d", Font_8x8);
      }
      if(mode==CRYPTO) {
        oled_print_inv(24,56, (char*) "  Crypto mode", Font_8x8);
      } else {
        oled_print_inv(24,56, (char*) "    Disk mode", Font_8x8);
      }
      oled_print_inv(0,0, (char*) " PITCHFORK!!5!  ", Font_8x8);
      if(userec) {
        oled_print_inv(128-(userec->len)*8,8, (char*) userec->name, Font_8x8);
      }
      for(i=0;i<MENU_LEN;i++) {
        oled_print(12,8*i+16, (char*) menuitems[i], Font_8x8);
        if(i==menuidx) {
          oled_print_inv(0,8*i+16, (char*) ">", Font_8x8);
        } else {
          oled_print(0,8*i+16, (char*) " ", Font_8x8);
        }
      }
      refresh=0;
    }
  }

  rsp_finish();
}
